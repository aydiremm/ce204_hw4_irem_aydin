package ce204_hw4_app.Menus;

import java.util.Scanner;

import ce204_hw4_app.App;
import ce204_hw4_app.Main;

/**
 * @name Menu
 *
 * @brief This abstract class provides a method or function to access or retrieve a reference to a paste button in a user interface.
 *
 * The method or function would depend on the programming language, framework, or library being used.
 * It is typically used to obtain a reference to the paste button object, allowing developers to manipulate its
 * appearance or functionality.
 */
public abstract class Menu {
	
	
	/**
	 * @brief Constructor method for the Menu class.
	 *
	 * @param app An instance of the App class that holds the reference to the application.
	 */
	protected App app;
	protected Scanner scanner;
	
	public Menu(App app) {
		this.app = app;
		this.scanner = app.scanner;
	}
	
	public abstract void run();
	
	
}
